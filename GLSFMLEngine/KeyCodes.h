#ifndef GLSFMLEngine_KeyCodes_h
#define GLSFMLEngine_KeyCodes_h


/*
 Special Key codes:
 Up     = 101
 Down   = 103
 Left   = 100
 Right  = 102
 */


typedef enum keycodes_t{
    kup     = 101,
    kdown   = 103,
    kleft   = 100,
    kright  = 102,
    kesc    = 27,
    kspace  = 32
} keycodes_t;


#endif
